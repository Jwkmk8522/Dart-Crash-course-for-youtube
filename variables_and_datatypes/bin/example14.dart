//Deffernt Types of Data Types

void main() {
  //Integer
  const int someInteger = 10;
  print(someInteger);

  //String
  const String someString = 'ali';
  print(someString);

  // double
  const double someDouble = 4.3;
  print(someDouble);

  //Boolean
  const bool someBoolean = true;
  print(!someBoolean);

  //List
  const List<int> someList = [1, 2, 3, 1];
  print(someList);
  //Maps
  const Map<String, String> someMaps = {'raja': 'rehman'};
  print(someMaps['raja']);
  //set
  const Set<int> someSet = {1, 2, 3};
  print(someSet);
  //Dynamic
}
